    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        vNIS.setText("NIS: "+jNIS.getText());
        vNAMA.setText("NAMA: "+jNAMA.getText());
        vJURUSAN.setText("JURUSAN "+jJURUSAN.getSelectedItem().toString());
        vALAMAT.setText("ALAMAT "+jALAMAT.getText());
        
        if (jLAKI.isSelected()){
            vJENISKELAMIN.setText("JENIS KELAMIN: LAKI-LAKI");
            
        }else if (pPEREMPUAN.isSelected()){
            vJENISKELAMIN.setText("JENIS KELAMIN: PEREMPUAN");
            
        }else {
            vJENISKELAMIN.setText("JENIS KELAMIN: TIDAK TERDEFINISI");
        }
        SimpleDateFormat sd = new SimpleDateFormat("yyyy-MM-dd");
        String tglLahir = String.valueOf(sd.format(jTANGGALLAHIR.getDate()));
        vTTL.setText("TTL: "+jTEMPATLAHIR.getText()+","+ tglLahir);
        
        String filename;
        try {
            filename = vPOTO.getText();
            String filepatch = "src/foto";
            File folder = new File(filepatch);
            if (!folder.exists()) {
                folder.mkdir();
                
            }
            File sourceFile = null;
            File destFile = null;
            String ekstensi = filename.substring(filename.lastIndexOf('.')+1);
            sourceFile = new File(filename);
            Date tglUpload = new Date();
            SimpleDateFormat fr = new SimpleDateFormat("yyyy-MM-dd-hh-mm-ss");
            String tglBaru = String.valueOf(fr.format(tglUpload));
            destFile = new File(filepatch + tglBaru.toString());
            Files.copy(sourceFile.toPath(),destFile.toPath());
            vPOTO.setText("POTO: "+ destFile.toString());
            
       } catch(Exception e) {
           JOptionPane.showMessageDialog(null,"UPLOAD ERROR"+ e);
       }
    }//GEN-LAST:event_jButton2ActionPerformed

