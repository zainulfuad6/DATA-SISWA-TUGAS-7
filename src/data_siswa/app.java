    private void jFOTOMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jFOTOMouseClicked
        try {
            JFileChooser chooser = new JFileChooser();
            chooser.showOpenDialog(null);
            File file = chooser.getSelectedFile();
            ImageIcon icon = new ImageIcon(file.toString());
            Image image = icon.getImage().getScaledInstance(jFOTO.getWidth(),jFOTO.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon ic = new ImageIcon(image);
            jFOTO.setText(null);
            jFOTO.setIcon(ic);
            String filename = file.getAbsolutePath();
            vPOTO.setText(filename);
            
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"upload error"+ e);
        }
    }//GEN-LAST:event_jFOTOMouseClicked

